<div id="helptext" style="display:none;">

	Sharing files in Athena works as follows...
	<ul>
		<li>You can add files to any Quote or Job.</li>

		<li>Any files you add here will be available only to the customer for
			whom the quote or job is for.</li>

		<li>Customers can also add files to any Quote or Job via the Customer
			Control Panel.</li>

		<li>You and the customer can see all the shared files on the View
			Quote or View Job pages.</li>

		<li>If you, or your customer, uploads a new file, Athena will send out
			an email to the other party to let them know the name of the file and
			which quote or job the file is for, and provide a link to login
			straight to the quote or job View page.</li>

		<li>For increased security, files will be available for one month from
			the time they are added to the system. After one month they will be
			deleted from the online system.</li>
	</ul>

</div>
