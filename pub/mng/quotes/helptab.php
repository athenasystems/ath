<div id="helptext" style="display: none;">

	<h2>Quoting with Athena</h2>
	<ul>
		<li>Quotes made with Athena and can be emailed directly, made into PDF
			files, or simply printed out for customer.</li>
		<li>Customers can take advantage of the Athena Customer Control Panel
			to initiate a request for quote from you, or agree a quote you have
			submitted, and share files to do with the work.</li>

		<li>However, customers do not need to use the Control Panel, and you
			can control the whole process without their involvement.</li>

		<li>Each quote can have several items on it, each of which can be
			turned into a working job.</li>
		<li>To create a new Quote, click "Add a New Quote" on the main Quotes
			page</li>
		<li>Tell the system the basic details about who the quote is for and
			the personnel involved on both sides.</li>
		<li>Then you can start adding items to the quote, each having their
			own details such as quantity and delivery date etc.</li>
		<li>Once a Quote is finished, you can "Make it Live" which means the
			Customer can log in to the Customer Control Panel and approve the
			quotation.</li>
		<li>Once a customer approves the quote in the Customer Control Panel,
			Athena will send you an email to let you know.</li>
		<li>Agreed quotes will be flagged on the front page in the "Jobs to
			Create" section, and also Quotes pages next to that quote.</li>
	</ul>

</div>
