<h2>How Jobs work in Athena</h2>

	<ul>
		<li>A Job represents one item from a Quote, that has been agreed by the customer, and is ready for business.</li>
		<li>Job are created from the View Quote page. Each Item on the quote has a link to "Create Job".</li>
		<li>Jobs can have several component parts, each with seperate dates and part and tool numbers.</li>
		<li>Jobs components are viewable on the Tcard, and are moved through the various stages of production.</li>
		<li>Once a Job is completed, you must click "Mark Finished", and then it will be available for Invoicing</li>
